package controlStatements;

public class ControlStatementForStatic {

	public static void main(String args[]) {
		
		System.out.println("Printing Numbers from 1 to 10" );
		for(int loops =1; loops<=10;loops++) {
			System.out.print(loops);
			System.out.print(" ");
			}
		System.out.println("" );
		
		System.out.println("Printing Numbers from 10 to 1" );
		for(int loops =10; loops>=1;loops--) {
			System.out.print(loops);
			System.out.print(" ");
			}
	
	}
}
