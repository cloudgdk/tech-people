package java_inheritence;

public class Calc {

	public int  add(int a, int b) {
		int addValue = a+b;
		return addValue;
	}
	
	public String  add(String atext, String btext) {
		String addValue = atext+btext;
		return addValue;
	}
	
	public int  add(int a, int b,int c) {
		int addValue = a+b+c;
		return addValue;
	}
	public int  add(int a, int b,int c,int d) {
		int addValue = a+b+c+d;
		return addValue;
	}
}
